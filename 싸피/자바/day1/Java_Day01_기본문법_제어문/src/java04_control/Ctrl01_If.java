public class Ctrl01_If {
    public static void main(String[] args) {
        
    	 int n = 15;
         if(n > 10) {
             System.out.println("블록 내부를 수행한다.");
         }//조건식이 참이므로 문장 실행
         
         if(n > 10)
             System.out.println("해당 문장을 수행한다.");
         //실행 문장 한개이므로 중괄호 생략
             System.out.println("얘도 수행?"); 
         // 이 문장은 조건문과 관련이 없다.
            
         
           
        
              
    }
}
